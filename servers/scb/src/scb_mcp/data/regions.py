REGIONS_BY_CODE: dict[str, Region] = {r.code: r for r in ALL_REGIONS}
REGIONS_BY_NAME: dict[str, Region] = {r.name.lower(): r for r in ALL_REGIONS}

_TRANSLATION = str.maketrans({
    "ä": "a", "å": "a", "ö": "o", "é": "e", "ü": "u",
    "Ä": "a", "Å": "a", "Ö": "o", "É": "e", "Ü": "u",
})


def normalize_for_search(text: str) -> str:
    """Normalisera sträng för fuzzy matching (tar bort svenska diakritiska tecken)."""
    lowered = text.lower().translate(_TRANSLATION)
    return "".join(ch for ch in lowered if ch.isalnum())


def search_regions(query: str) -> list[Region]:
    """Sök regioner med fuzzy matching.

    Stöder sökning utan svenska tecken (t.ex. "Goteborg" matchar "Göteborg").
    Returnerar en lista av träffar sorterade efter typ (land, län, kommun).
    """
    if not query:
        return []

    if code_match := REGIONS_BY_CODE.get(query):
        return [code_match]

    if name_match := REGIONS_BY_NAME.get(query.lower()):
        return [name_match]

    normalized = normalize_for_search(query)
    if not normalized:
        return []

    matches: list[Region] = []
    for region in ALL_REGIONS:
        nname = normalize_for_search(region.name)
        if normalized in nname or (len(normalized) >= 3 and nname in normalized) or query in region.code:
            matches.append(region)

    order = {"country": 0, "county": 1, "municipality": 2}
    matches.sort(key=lambda r: (order.get(r.type, 99), r.name))
    return matches


def find_region(query: str) -> Region | None:
    """Hitta bästa matchningen för en sökfråga."""
    matches = search_regions(query)
    if not matches:
        return None

    normalized = normalize_for_search(query)
    for r in matches:
        if normalize_for_search(r.name) == normalized or r.code == query:
            return r
    return matches[0]
